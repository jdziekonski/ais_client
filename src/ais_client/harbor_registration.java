    private void registerButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_registerButtonMouseClicked
            String nameTaker = nameField.getText();
            String countryTaker = countryField.getText();
            float latitudeTaker = Float.parseFloat(latitudeField.getText());
            float longitudeTaker = Float.parseFloat(longitudeField.getText());
            
            ShipService_Service serviceTwo = new ShipService_Service();
            serviceTwo.getShipServicePort().addHarbor(nameTaker, countryTaker, latitudeTaker, longitudeTaker);
            JOptionPane.showMessageDialog(null, "Harbor was added!");
            this.dispose();
    }//GEN-LAST:event_registerButtonMouseClicked

